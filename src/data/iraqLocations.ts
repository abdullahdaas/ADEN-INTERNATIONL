export const IRAQ_GOVERNORATES = Object.keys(iraqLocations);

export function getDistrictsByGovernorate(governorate: string): string[] {
  return iraqLocations[governorate] || [];
}

export interface IraqLocationData {
  governorate: string;
  districts: {
    name: string;
    subDistricts: {
      name: string;
      neighborhoods: string[];
    }[];
  }[];
}

// Compatibility export for legacy consumers that still expect nested data.
export const IRAQ_LOCATIONS: IraqLocationData[] = IRAQ_GOVERNORATES.map((governorate) => ({
  governorate,
  districts: getDistrictsByGovernorate(governorate).map((district) => ({
    name: district,
    subDistricts: []
  }))
}));

export const IRAQ_BOUNDS = {
  north: 37.3780,
  south: 29.0653,
  east: 48.5667,
  west: 38.7923
};

export function isLocationInIraq(lat: number, lng: number) {
  return lat >= IRAQ_BOUNDS.south && lat <= IRAQ_BOUNDS.north &&
         lng >= IRAQ_BOUNDS.west && lng <= IRAQ_BOUNDS.east;
}
